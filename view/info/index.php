<h2>Rules &amp; General info</h2>
<p></p>
<ul>
    <li>You win The DreamHack Game by answering questions as fast as possible - Not by cheating!</li>
    <li>It is not allowed to tear down clues, try to hack the server or access any restricted areas.</li>
    <li>You are encouraged compete in teams, but please try to keep the team size to a maximum of 10 people.</li>
    <li>You must have a seat ticket at DreamHack to be able to win</li>
    <li>All passwords are case-insensitive, it doesn't matter if you have capital letters or not!</li>
    <li>The passwords are never completely random letters and numbers, but can be a combination of words and numbers or hexadecimal and binary digits.</li>
    <li>The most important rule is that you are not allowed to ruin for anyone else. Leave visited places as they were when you got there, don't destroy clues.</li>
    <li>Never enter into restricted areas, you will never need to enter an area not open to the public.</li>
    <li>Do not post tasks, answers or clues in the IRC channel #dhgame @ quakenet</li>
    <li>If you seem to have a problem of a technical nature, please contact the DreamHack Game crew. You can find us in #dhgame @ quakenet.</li>
</ul>

<h2>
	Help</h2>
<p>
	There is a clue hotline on skype if you call skype contact &quot;dhg.clueline&quot;. It is an answering machine to which you describe which episode and task you&#39;re on and what you have done so far. Be detailed but motivate why you have taken each step. You may speak Swedish. Try calling even if it looks like it is offline. If your reasoning is sound we will reward the call with a clue on the question, on the web page of the question.<br />
	&nbsp;</p>
<p>
	The DreamHack game can be difficult and frustrating at times. This is part of the game. The ultimate goal of the DreamHack Game challenge is to be calm and think simple while trying to be the fastest thinker out there. It&#39;s often not as hard as you think it is. Head over to <strong>#dhgame @ quakenet</strong> on IRC to hang out with the creators and other players.</p>
<h3>
	For beginners</h3>
<p>
	Welcome to the exciting world of The DreamHack Game!</p>
<p>
	The game is all about figuring out what to do. As you play more you will get more and more into the spirit of the game and you will figure things out faster. It is important to play in a team or you&#39;ll have a difficult task finishing the game.</p>
<p>
	If you have general questions about the game, we in the DHGame-crew are happy to help. Just talk to us in <strong>#dhgame @ quakenet</strong>, or if you manage to find us in person :) The IRC channel is also very helpful for finding people to play with or to talk generally about the game. <em>However you must NOT post the tasks, clues or answers in the channel #dhgame</em>.</p>
<p>
	Good things to think about when trying to solve the game is numbers (count stuff), patterns and odd things that don&#39;t fit in. Tasks can be computer-related, but don&#39;t have to be, many times you will have to leave your seat and run to find clues. You might even have to interact with people, software or machines to be able to solve a task.</p>
